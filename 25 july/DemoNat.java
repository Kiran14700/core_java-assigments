class DemoNat
  {
   public static void main (String args[])
     {
       int i,sum=0;
       System.out.println("The first 10 natural numbers are:");
       for(i=1;i<=10;i++) 
        {
         sum=sum+i;
         System.out.println("The sum of this numbers are:"+sum);
        }
         System.out.println("The sum of this numbers are:"+sum);
     }
  }